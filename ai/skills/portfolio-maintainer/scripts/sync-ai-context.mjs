#!/usr/bin/env node

import crypto from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "../../../..");
const statePath = path.join(rootDir, "ai/skills/portfolio-maintainer/references/project-state.md");
const shouldWrite = process.argv.includes("--write");

const readText = async (relativePath) => {
  try {
    return await fs.readFile(path.join(rootDir, relativePath), "utf8");
  } catch {
    return "";
  }
};

const readJson = async (relativePath) => {
  const text = await readText(relativePath);
  if (!text) return null;

  try {
    return JSON.parse(text);
  } catch {
    return null;
  }
};

const fileInfo = async (relativePath) => {
  const absolutePath = path.join(rootDir, relativePath);

  try {
    const content = await fs.readFile(absolutePath);
    const stats = await fs.stat(absolutePath);
    return {
      path: relativePath,
      bytes: stats.size,
      mtime: stats.mtime.toISOString(),
      sha256: crypto.createHash("sha256").update(content).digest("hex").slice(0, 16),
    };
  } catch {
    return {
      path: relativePath,
      missing: true,
    };
  }
};

const markdownFiles = async () => {
  const files = [
    "AGENTS.md",
    "TODO.md",
    "portfolio_backend/README.md",
    "portfolio_frontend/README.md",
    "ai/skills/portfolio-maintainer/SKILL.md",
    "ai/skills/portfolio-maintainer/references/project-rules.md",
  ];

  return Promise.all(files.map(fileInfo));
};

const packageSummary = (packageJson) => {
  if (!packageJson) return null;

  return {
    name: packageJson.name,
    scripts: packageJson.scripts ?? {},
    dependencies: Object.keys(packageJson.dependencies ?? {}).sort(),
    devDependencies: Object.keys(packageJson.devDependencies ?? {}).sort(),
  };
};

const extractRoutes = (content) => {
  const routes = [];
  const routePattern = /<Route\s+path="([^"]+)"/g;
  let match = routePattern.exec(content);

  while (match) {
    routes.push(match[1]);
    match = routePattern.exec(content);
  }

  return routes;
};

const extractProjectColumns = (initSql) => {
  const createTableMatch = initSql.match(/CREATE TABLE `projects` \(([\s\S]*?)\n\)/);
  const columns = new Set();

  if (createTableMatch) {
    for (const line of createTableMatch[1].split("\n")) {
      const columnMatch = line.match(/^\s+`([^`]+)`/);
      if (columnMatch) columns.add(columnMatch[1]);
    }
  }

  const alterMatches = initSql.matchAll(/ADD COLUMN `([^`]+)`/g);
  for (const match of alterMatches) {
    columns.add(match[1]);
  }

  return [...columns].sort();
};

const extractFeaturedSeeds = (initSql) => {
  const seeds = [];
  const seedPattern = /UPDATE `projects` SET\s+`is_featured` = 1,\s+`featured_order` = (\d+)\s+WHERE `title` = '([^']+)'/g;
  let match = seedPattern.exec(initSql);

  while (match) {
    seeds.push({
      title: match[2],
      featured_order: Number(match[1]),
    });
    match = seedPattern.exec(initSql);
  }

  return seeds.sort((first, second) => first.featured_order - second.featured_order);
};

const renderList = (items, formatter) => (
  items.length > 0 ? items.map(formatter).join("\n") : "- Aucun"
);

const render = async () => {
  const frontendPackage = packageSummary(await readJson("portfolio_frontend/package.json"));
  const backendPackage = packageSummary(await readJson("portfolio_backend/package.json"));
  const appRoutes = extractRoutes(await readText("portfolio_frontend/src/App.tsx"));
  const initSql = await readText("db/init.sql");
  const projectColumns = extractProjectColumns(initSql);
  const featuredSeeds = extractFeaturedSeeds(initSql);
  const docs = await markdownFiles();
  const dockerCompose = await readText("docker-compose.yml");

  return `# Project State

Generated by \`ai/skills/portfolio-maintainer/scripts/sync-ai-context.mjs --write\`.

## Sync

- Generated at: ${new Date().toISOString()}
- Root: \`${rootDir}\`
- Docker ports: ${dockerCompose.includes("3308:3306") ? "MySQL local 3308, Docker 3306" : "Check docker-compose.yml"}

## Markdown Files

${renderList(docs, (doc) => (
  doc.missing
    ? `- \`${doc.path}\`: missing`
    : `- \`${doc.path}\`: ${doc.bytes} bytes, sha256 ${doc.sha256}, mtime ${doc.mtime}`
))}

## Frontend

- Package: \`${frontendPackage?.name ?? "unknown"}\`
- Scripts: ${Object.keys(frontendPackage?.scripts ?? {}).map((script) => `\`${script}\``).join(", ")}
- Routes: ${appRoutes.map((route) => `\`${route}\``).join(", ")}
- Key dependencies: ${["react", "react-router-dom", "react-hook-form", "zod", "tailwindcss", "vite"].filter((dependency) => frontendPackage?.dependencies.includes(dependency) || frontendPackage?.devDependencies.includes(dependency)).join(", ")}

## Backend

- Package: \`${backendPackage?.name ?? "unknown"}\`
- Scripts: ${Object.keys(backendPackage?.scripts ?? {}).map((script) => `\`${script}\``).join(", ")}
- Key dependencies: ${["express", "mysql2", "jsonwebtoken", "nodemailer", "multer", "express-validator"].filter((dependency) => backendPackage?.dependencies.includes(dependency) || backendPackage?.devDependencies.includes(dependency)).join(", ")}

## Database

- Source of truth: \`db/init.sql\`
- Project columns: ${projectColumns.map((column) => `\`${column}\``).join(", ")}
- Home carousel seed:
${renderList(featuredSeeds, (seed) => `  - ${seed.featured_order}. ${seed.title}`)}

## Required Validation

- Frontend: \`cd portfolio_frontend && npm run lint && npm run build\`
- Backend: \`cd portfolio_backend && npm run lint && npm run build\`
`;
};

const content = await render();

if (shouldWrite) {
  await fs.writeFile(statePath, content);
  console.log(`Updated ${path.relative(rootDir, statePath)}`);
} else {
  console.log(content);
}
